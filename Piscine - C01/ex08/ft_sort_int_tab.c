void	ft_sort_int_tab(int *tab, int size)
{
	int	i;
	int	j;
	int	aux;

	i = 0;
	while (i < size - 1)
	{
		if (tab[i] > tab[i + 1])
		{
			j = i;
			while (j < size - 1)
			{
				aux = tab[j];
				tab[j] = tab [j + 1];
				tab[j + 1] = aux;
				j++;
			}
			i = -1;
		}
		i++;
	}
}
